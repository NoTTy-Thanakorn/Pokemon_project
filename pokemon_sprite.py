ACCENT = {
    "grass":(140,230,60),"fire":(255,200,40),"water":(120,210,255),
    "electric":(255,250,80),"psychic":(255,120,215),"rock":(205,185,125),
    "ghost":(190,140,255),"normal":(225,215,195),"legend":(255,230,60),
}

def _blend(a,b,t): return tuple(int(a[i]+(b[i]-a[i])*t) for i in range(3))

def create_mon_sprite(color, ptype="normal", size=32):
    tmpl = T.get(ptype, T["normal"])
    acc  = ACCENT.get(ptype,(200,200,200))
    pal  = {
        'B':color, 'D':_blend(color,(0,0,0),0.38),
        'H':_blend(color,(255,255,255),0.55),
        'O':(12,8,18),'О':(12,8,18),
        'E':(248,248,248),'P':(12,12,45),
        'A':acc,'А':acc,
        'b':color,
        '_':None,' ':None,
    }
    surf=pygame.Surface((32,32),pygame.SRCALPHA); surf.fill((0,0,0,0))
    for ri,row in enumerate(tmpl[:32]):
        for ci,ch in enumerate(row[:32]):
            col=pal.get(ch)
            if col: surf.set_at((ci,ri),(*col,255))
    if size!=32: surf=pygame.transform.scale(surf,(size,size))
    return surf

def create_boss_sprite(color,size=128):
    return create_mon_sprite(color,"legend",size)